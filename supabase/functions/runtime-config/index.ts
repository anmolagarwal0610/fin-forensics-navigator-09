import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  // Handle CORS preflight
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const backendUrl = Deno.env.get("LIVE_BACKEND_URL");
    
    if (!backendUrl) {
      console.error("LIVE_BACKEND_URL not configured in Edge Function secrets");
      return new Response(
        JSON.stringify({ error: "Backend URL not configured" }),
        { 
          status: 500, 
          headers: { ...corsHeaders, "Content-Type": "application/json" } 
        }
      );
    }

    console.log(`Runtime config requested, returning backend URL: ${backendUrl.substring(0, 20)}...`);

    return new Response(
      JSON.stringify({ apiBase: backendUrl }),
      { 
        status: 200,
        headers: { ...corsHeaders, "Content-Type": "application/json" } 
      }
    );
  } catch (error) {
    console.error("Error in runtime-config:", error);
    return new Response(
      JSON.stringify({ error: "Internal server error" }),
      { 
        status: 500, 
        headers: { ...corsHeaders, "Content-Type": "application/json" } 
      }
    );
  }
});
